<template>
  <div>
    <h3>Formulario</h3>
    <form @submit.prevent="validarForm">
      <label for="correo">Correo: </label>
      <input v-model="email" id="correo" type="text" required />
      <label for="contrase単a">Contrase単a: </label>
      <input
        v-model="password"
        placeholder="introduce tu contrase単a"
        id="contrase単a"
        type="password"
        required
      />
      <button type="submit">Validar</button>
    </form>
    
  </div>
</template>
<script>
import Firebase from "firebase";
export default {
  // datos para login: usuario1@gmail.com || 123456
  data: () => ({
    email: "",
    password: "",
  }),
   // Redireccionar a home cuando se valide formulario
  methods: {
    validarForm() {
      Firebase.auth()
        .signInWithEmailAndPassword(this.email, this.password)
        .then((response) => {
          this.$router.push("Home");
        })
        .catch((response) => {
          console.error(response);
        });
    },
  },
